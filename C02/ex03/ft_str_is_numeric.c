#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	c;
	int	v;

	c = 0;
	v = 1;
	while (str[c] != '\0')
	{
		if ((str[c] >= '0') && (str[c] <= '9'))
			v = 1;
		else
			return (0);
		c++;
	}
	return (v);
}
